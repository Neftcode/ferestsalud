    /**
     * @Route("/pdf", name="pdf")
     */
    public function index(Request $request)
    {
        $projectDir = $this->getParameter('kernel.project_dir') . '/public/';
        $pdfClass = $request->query->get('pdf');
        $data = [];
        if ($pdfClass=="Factura") {
            $pdf = new FacturaBundle($projectDir);
            $data = $this->factura;
        } elseif ($pdfClass=="ValoraciónCodo") {
            $pdf = new ValoracionCodoBundle($projectDir);
            $data = $this->valoracionCodo;
        } elseif ($pdfClass=="EvoluciónDiaria") {
            $pdf = new EvolucionDiariaBundle($projectDir);
            $data = $this->evolucionDiaria;
        } elseif ($pdfClass=="OrdenMédica") {
            $pdf = new OrdenMedicaBundle($projectDir);
            $data = $this->ordenmedica;
        } elseif ($pdfClass=="FURIPS") {
            $pdf = new FuripsBundle($projectDir);
            $data = $this->furips;
        } else {
            return $this->redirectToRoute('admin');
        }
        $pdf->pdfBuild($data);
        $pdf->Output('I', $pdf->getName(), true);
    }

